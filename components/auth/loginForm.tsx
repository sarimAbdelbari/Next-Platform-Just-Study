"use client";
import { cn } from "@/lib/utils";
import { Button } from "@/components/ui/button";
import { Card, CardContent } from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import Image from "next/image";
import Link from "next/link";
import Github from "@/components/providers/github";
import Google from "@/components/providers/google";
import { loginCredentials } from "@/lib/providers/auth"
import { useActionState } from 'react'
import { Checkbox } from "@/components/ui/checkbox"
import { errorToast, successToast } from "@/components/utils/toastNotification";

import { useRouter } from "next/navigation";
import { useEffect  } from "react";

export function LoginForm({
  className,
  ...props
}: React.ComponentProps<"div">) {
  type State = {
    success?: boolean;
    error?: string;
    message?: string;
  };

  const initialState: State = {};

  const loginAction = async (
    prevState: State, 
    payload: FormData
  ): Promise<State> => {
    return loginCredentials(payload);
  };

  const [state, formAction, isPending] = useActionState(loginAction, initialState);
  const router = useRouter();

  useEffect(() => {
    if (state?.success) {
      successToast(state?.message ?? "Login successful");
      router.push('/main/user/files');
    } else if (state?.error) {
      errorToast(state.error);
    }
  }, [state, router]);


  return (
    <div className={cn("flex flex-col gap-6", className)} {...props}>
      <Card className="overflow-hidden">
        <CardContent className="grid p-0 md:grid-cols-2">
          <div className="p-6 md:p-8">
            <div className="flex flex-col gap-6">
              <div className="flex flex-col items-center text-center">
                <h1 className="text-2xl font-bold">Welcome back</h1>
                <p className="text-balance text-muted-foreground">
                  Login to your Acme Inc account
                </p>
              </div>
             <form action={formAction} className="flex flex-col gap-6">
             <div className="grid gap-2">
                <Label htmlFor="email">Email</Label>
                <Input
                  id="email"
                  type="email"
                  name="email"
                  placeholder="m@example.com"
                  required
                />
              </div>
              <div className="grid gap-2">
                <div className="flex items-center">
                  <Label htmlFor="password">Password</Label>
                  <a
                    href="#"
                    className="ml-auto text-sm underline-offset-2 hover:underline"
                  >
                    Forgot your password?
                  </a>
                </div>
                <Input id="password" name="password" type="password" required />

                

              </div>
              <div className="items-center flex gap-2">
      <Checkbox id="rememberMe" name="rememberMe"  />
      <div className="grid gap-1.5 leading-none">
        <label
          htmlFor="rememberMe"
          className="text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70"
        >
          Remamber Me
        </label>
        
      </div>
                {/* <div className="items-top flex space-x-2">
      <Checkbox id="RemamberMe"  />
      <div className="grid gap-1.5 leading-none">
        <label
          htmlFor="terms1"
          className="text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70"
        >
          Accept terms and conditions
        </label>
        <p className="text-sm text-muted-foreground">
          You agree to our Terms of Service and Privacy Policy.
        </p>
      </div> */}
    </div>
              <Button type="submit" disabled={isPending} className="w-full">
                Login
              </Button>
             </form>
              <div className="relative text-center text-sm after:absolute after:inset-0 after:top-1/2 after:z-0 after:flex after:items-center after:border-t after:border-border">
                <span className="relative z-10 bg-background px-2 text-muted-foreground">
                  Or continue with
                </span>
              </div>
              <div className="grid grid-cols-2 gap-4">
                <Github/>
                <Google/>
              </div>
              
              <div className="text-center text-sm">
                Don&apos;t have an account?{" "}
                <Link href="/register" className="underline underline-offset-4">
                  Sign up
                </Link>
              </div>
            </div>
          </div>
          <div className="relative hidden bg-muted md:block h-full">
            <Image
              src="/assets/images/placeHolder.jpg"
              width={450}
              height={450}
              alt="Image"
              className="absolute  inset-0 h-full w-full object-cover dark:brightness-[0.2] dark:grayscale"
            />
          </div>
        </CardContent>
      </Card>
      <div className="text-balance text-center text-xs text-muted-foreground [&_a]:underline [&_a]:underline-offset-4 hover:[&_a]:text-primary">
        By clicking continue, you agree to our <a href="#">Terms of Service</a>{" "}
        and <a href="#">Privacy Policy</a>.
      </div>
    </div>
  );
}
